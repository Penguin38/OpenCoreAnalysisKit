std::string EventTags::ConvertTags(int id) {
    for (int i = 0; i < sizeof(kEventTags)/sizeof(kEventTags[0]); i++) {
        if (kEventTags[i].id != id)
            continue;

        return kEventTags[i].tag;
    }
    return std::to_string(id);
}

